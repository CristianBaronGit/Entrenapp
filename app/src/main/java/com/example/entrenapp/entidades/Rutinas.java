package com.example.entrenapp.entidades;

public class Rutinas {

    private int id_registro;
    private String fecha_entreno;
    private String hora_entreno;

    public int getId_registro() {
        return id_registro;
    }

    public void setId_registro(int id_registro) {
        this.id_registro = id_registro;
    }

    public String getFecha_entreno() {
        return fecha_entreno;
    }

    public void setFecha_entreno(String fecha_entreno) {
        this.fecha_entreno = fecha_entreno;
    }

    public String getHora_entreno() {
        return hora_entreno;
    }

    public void setHora_entreno(String hora_entreno) {
        this.hora_entreno = hora_entreno;
    }
}

